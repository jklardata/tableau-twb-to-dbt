export function generateAuditMarkdown(result) {
  const { meta, summary, fields } = result;
  const lines = [];

  lines.push(`# Audit Report: ${meta.workbook_name}`);
  lines.push(`\n> Generated by TableautoDbt Audit | ${meta.audited_at}\n`);

  lines.push(`## Health Score: ${meta.health_score}/100\n`);
  lines.push(`| Metric | Count |`);
  lines.push(`|---|---|`);
  lines.push(`| Total Calculated Fields | ${meta.total_calculated_fields} |`);
  lines.push(`| Errors | ${meta.issue_breakdown.error} |`);
  lines.push(`| Warnings | ${meta.issue_breakdown.warning} |`);
  lines.push(`| Info | ${meta.issue_breakdown.info} |`);
  lines.push(`\n---\n`);

  lines.push(`## Summary\n`);
  if (summary.unused_fields.length > 0) lines.push(`- **Unused Fields (${summary.unused_fields.length})**: ${summary.unused_fields.join(", ")}`);
  if (summary.high_complexity_fields.length > 0) lines.push(`- **High Complexity (${summary.high_complexity_fields.length})**: ${summary.high_complexity_fields.join(", ")}`);
  if (summary.performance_issues.length > 0) lines.push(`- **Performance Issues (${summary.performance_issues.length})**: ${summary.performance_issues.join(", ")}`);
  if (summary.circular_dependencies.length > 0) lines.push(`- **Circular Dependencies**: ${summary.circular_dependencies.join(", ")}`);
  lines.push(`\n---\n`);

  const fieldsWithIssues = fields.filter(f => f.issues.length > 0);
  if (fieldsWithIssues.length > 0) {
    lines.push(`## Fields Needing Attention (${fieldsWithIssues.length})\n`);
    for (const f of fieldsWithIssues) {
      lines.push(`### ${f.caption}`);
      lines.push(`- **Datasource**: ${f.datasource}`);
      lines.push(`- **Complexity**: ${f.complexity_label} (score: ${f.complexity_score}, nesting: ${f.nesting_depth})`);
      if (f.unused) lines.push(`- **Unused**: Yes`);
      if (f.dependencies.length > 0) lines.push(`- **Dependencies**: ${f.dependencies.join(", ")}`);
      lines.push(`\n**Issues**:`);
      for (const issue of f.issues) lines.push(`- [${issue.severity.toUpperCase()}] ${issue.message}`);
      lines.push(`\n**Formula**:\n\`\`\``);
      lines.push(f.formula);
      lines.push(`\`\`\`\n`);
      lines.push(`---\n`);
    }
  }

  lines.push(`## All Calculated Fields (${fields.length})\n`);
  lines.push(`| Field | Datasource | Complexity | Nesting | Issues | Unused |`);
  lines.push(`|---|---|---|---|---|---|`);
  for (const f of fields) {
    lines.push(`| ${f.caption} | ${f.datasource} | ${f.complexity_label} | ${f.nesting_depth} | ${f.issues.length} | ${f.unused ? "Yes" : "No"} |`);
  }

  return lines.join("\n");
}

